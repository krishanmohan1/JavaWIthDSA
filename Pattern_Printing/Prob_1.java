package Pattern_Printing;

public class Prob_1 {
    public static void main(String[] args) {

        pattern_1(4);


    }




    // printing a square of star of n rows
    static void pattern_1(int n){
        for(int i = 0; i<n;i++){
            for(int j = 0; j<n;j++){
                System.out.print("* ");
            }
            System.out.println();
        }
    }
}

