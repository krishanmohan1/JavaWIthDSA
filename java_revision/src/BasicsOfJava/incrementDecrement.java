package BasicsOfJava;

public class incrementDecrement {
    public static void main(String[] args) {
        int x=5;
        System.out.println(x++); // esko post increment kehte hai
        /* post me pahle print karo then container me update karke 1 one increase karo*/

        System.out.println(x);

        System.out.println(++x); // esko pre increment kehte hai
        /* esme pahle  variable container me update karke 1 update karo then
        then usko updated value ko print karo
         */
        System.out.println(x);

    }
}
